import { BuiltinTypes, ElemID, ObjectType } from '@salto-io/adapter-api'
import { ASSESET_SCHEMA_TYPE, JIRA } from '../../../constants'

export const createAssestsSchemaTypes = (): {
    assestSchemaType: ObjectType
    subTypes: ObjectType[]
  } => {
  const assestSchemaType = new ObjectType({
    elemID: new ElemID(JIRA, ASSESET_SCHEMA_TYPE),
    fields: {
      workspaceId: { refType: BuiltinTypes.STRING },
      globalId: { refType: BuiltinTypes.STRING },
      id: { refType: BuiltinTypes.STRING },
      name: { refType: BuiltinTypes.STRING },
      description: { refType: BuiltinTypes.STRING },
      objectSchemaKey: { refType: BuiltinTypes.STRING },
      status: { refType: BuiltinTypes.STRING },
    },
  })
  return { assestSchemaType, subTypes: [] }
}
